/*### 5. Реализовать основные 4 арифметические операции в виде функций с двумя параметрами. 
Обязательно использовать оператор return. */

'use strict'

function sum(a,b) {
    return a + b 
}

function subtraction (a,b) {
    return a - b 
}

function multiplication (a,b) {
    return a * b 
}

function division (a,b) {
    return a / b 
}

